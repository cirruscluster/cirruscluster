import json
import yaml

class FilterModule(object):
    ''' Ansible core jinja2 filters '''

    def filters(self):
        return {
            'to_json': json.dumps,
            'from_json': json.loads,
            'to_yaml': yaml.dump,
            'from_yaml': yaml.load,
        }
    
